from turtle import *

screen = Screen()
screen.bgcolor("lightblue")
screen.title("my name")
screen.setup(width = 1000,height = 500)

steven = Turtle()
steven.color("green")
steven.shape("turtle")
steven.speed(0)

# east is 0 north is 90 west is 180 and south is 270 or -90


steven.penup()
steven.goto(-400,-100)
steven.pendown()
steven.goto(-300,100)
steven.goto(-250,-100)
steven.penup()
steven.goto(-360,-10)
steven.pendown()
steven.forward(100)
steven.penup()
steven.goto(-200,-100)
steven.pendown()
steven.goto(-200,0)
steven.goto(-250,100)
steven.penup()
steven.goto(-200,0)
